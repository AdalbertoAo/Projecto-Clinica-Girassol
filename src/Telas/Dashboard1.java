    private void setData(){
        LocalDate data =  LocalDate.now();
        int ano = data.getYear();
        int mes = data.getMonthValue();
        int dia = data.getDayOfMonth();
        String [] meses = {"Janeiro", "Fevereiro", "Mar√ßo", "Abril", "Maio", "Junho", "Julho", "Agosto", "Setembro", "Outubro", 
            "Novembro", "Dezembro"};
        Data_actual.setText("Hoje, "+dia+" de "+meses[mes -1] +" de "+ano);
    }
